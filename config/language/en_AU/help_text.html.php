<h3>Login</h3>
Is your institution connected to {cfg:site_name}? Choose Login and select your institution from the list of institutions. Is your institution not listed or can't you log in? Please contact your institution's helpdesk. 
Are you logged in? You can now:

<ul class="fa-ul">
    <li><i class="fa-li fa fa-caret-right"></i>Upload 1 or more files (encrypted). </li> 
    <li><i class="fa-li fa fa-caret-right"></i>Receive a message when your upload is ready.</li>
    <li><i class="fa-li fa fa-caret-right"></i>Receive a download link.</li>
    <li><i class="fa-li fa fa-caret-right"></i>Invite guest users.</li>
</ul>



<h3>Guest users</h3>
<p>Did you receive an invitation link you weren't expecting? Before you get started, make sure all links in it actually point to {cfg:site_name}. The invitation may not be legitimate. 
</p>
<p>
Were you expecting an invitation? Simply upload files with the information in the invitation email and share the download link. 
</p>
<p>
Not receiving a download link? The user who invited you to this system might have done so allowing you to upload files and get a link to allow other folks to download those files. If you can not get a link then you will have to provide email address(es) of the people who you wish to invite to download the uploaded files.
	</p>

<h3>Upload</h3>
<p>Is your upload interrupted? Simply upload the same file again. The progress indicator will then pick up where your interrupted upload left off.
</p>

<p>Did your file change in the meantime? Then rename it before you start a new upload. That way the upload will start at the beginning of the new file.
</p>

